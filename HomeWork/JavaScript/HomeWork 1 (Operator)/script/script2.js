const num = prompt("Enter Number")

switch (Number(num)) {
    case (0):
        alert("0 is )");
    break;

    case (1):
        alert("1 is !");
    break;

    case (2):
        alert("2 is @");
    break;
    
    case (3):
        alert("3 is #");
    break;

    case (4):
        alert("4 is $");
    break;

    case (5):
        alert("5 is %");
    break;

    case (6):
        alert("6 is ^");
    break;

    case (7):
        alert("7 is &");
    break;
    
    case (8):
        alert("8 is *");
    break;

    case (9):
        alert("9 is (");
    break;
}